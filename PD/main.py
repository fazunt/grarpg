class Pierwiastek1:
    def __init__(self, _nazwa, _symbol, _elektro_ujemność, _masa_atomowa, _liczba_atomowa) -> None:
        self.nazwa = _nazwa
        self.symbol = _symbol
        self.elektro_ujemność = _elektro_ujemność
        self.masa_atomowa = _masa_atomowa
        self.liczba_atomowa = _liczba_atomowa

    def inf(self):
        print(f"nazwa {self.nazwa}")
        print(f"symbol {self.symbol}")
        print(f"elektro ujemność {self.elektro_ujemność}")
        print(f"masa atommowa {self.masa_atomowa}")
        print(f"liczba atomowa {self.liczba_atomowa}")

p1 = Pierwiastek1("glin", "Al", "1,5", "26.98", "13")
p1.inf()


